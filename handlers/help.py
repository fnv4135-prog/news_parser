import logging
from aiogram import Router
from aiogram.filters import Command
from aiogram.types import Message

router = Router()


@router.message(Command("help"))
async def cmd_help(message: Message):
    user_id = message.from_user.id
    logging.info(f"User {user_id} requested /help")

    help_text = (
        "📖 <b>Инструкция по использованию бота</b>\n\n"
        "<b>Важно соблюдать этапность!</b> Если что-то не работает, проверь выполнение предыдущих шагов.\n\n"
        "🔹 <b>1. Настройка городов</b>\n"
        "   • /cities → «➕ Добавить город» → введите название (например, «Великий Новгород»).\n"
        "   • Город нужен, чтобы группировать источники и каналы публикации.\n\n"
        "🔹 <b>2. Добавление источников новостей</b>\n"
        "   • /sources → выберите город → «➕ Добавить источник» → выберите тип (VK, Telegram, RSS).\n"
        "   • Для VK введите ID группы (например, club215921691 или novgorod_life).\n"
        "   • Для Telegram введите username канала (например, @novgorodtop) или invite-ссылку.\n"
        "   • Для RSS введите URL ленты (например, https://news.yandex.ru/Novgorod.rss).\n\n"
        "🔹 <b>3. Добавление каналов для публикации</b>\n"
        "   • /add_channel → выберите город → отправьте username канала (например, @news_channel).\n"
        "   • Для приватных каналов — перешлите любое сообщение из канала.\n"
        "   • <b>Важно:</b> бот должен быть администратором этого канала.\n\n"
        "🔹 <b>4. Парсинг новостей</b>\n"
        "   • После добавления источников выполните /parse_now — бот соберёт новости.\n"
        "   • Парсинг также происходит автоматически каждые 15 минут.\n\n"
        "🔹 <b>5. Просмотр и публикация постов</b>\n"
        "   • /posts → выберите город → выберите пост.\n"
        "   • Вы можете отредактировать текст, заменить фото, выбрать каналы (несколько).\n"
        "   • Перед публикацией можно изменить подпись для этого поста.\n"
        "   • Опубликовать сейчас или отложить на нужное время.\n\n"
        "🔹 <b>6. Рекламные сообщения</b>\n"
        "   • /ad → отправьте текст, затем фото/видео (или /skip), выберите город и каналы.\n"
        "   • Подпись канала добавится автоматически.\n\n"
        "🔹 <b>7. Управление каналами публикации</b>\n"
        "   • /channels → выберите город → выберите канал → «✏️ Изменить подпись».\n"
        "   • Подпись добавляется в конец каждого поста при публикации.\n\n"
        "🔹 <b>8. Отложенные посты</b>\n"
        "   • /scheduled — список запланированных постов. Нажмите на любой, чтобы отменить.\n\n"
        "❓ <b>Частые ошибки и решения</b>\n"
        "   • «Нет добавленных городов» → создайте город через /cities.\n"
        "   • «Нет каналов для публикации» → добавьте канал через /add_channel.\n"
        "   • «Пост не найден» → нажмите «🔄 Обновить» или запустите /parse_now.\n\n"
        "📌 Если проблема не решается, напишите разработчику @NicholasBiz."
    )
    await message.answer(help_text, parse_mode="HTML")
    await message.delete()